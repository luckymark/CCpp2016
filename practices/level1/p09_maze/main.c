#include <stdio.h>
#include <stdlib.h>
#define EXIT_X 15
#define EXIT_y 32

//设计迷宫
char maze[100][100]={"######################################",
                     "######################      ###  #####",
                     "O  ################### #### ##        ",
                     "## ###         ####### ####   ########",
                     "## ### ####### ####### ####### #######",
                     "## ### ####### #            ##  ######",
                     "## ### ####### #######################",
                     "##     #######      ##################",
                     "############## ### ###################",
                     "############## ### ###################",
                     "########       #       ####      #####",
                     "####### ############## #### #### #####",
                     "####### ##### ##   ### #### #### #####",
                     "####### ##### #### ### #### #### #####",
                     "####### ##### #### ###      #### #####",
                     "####### ##### #### ############# #####",};
void showMaze();
int main()
{
    showMaze(maze);
    //控制辨识
    int x=2,y=0;
    while(x!=EXIT_X||y!=EXIT_y)
    {
        char input=getch();
        if(input=='w')
        {
            if(maze[x-1][y]==' ')
            {
                maze[x-1][y]='O';
                maze[x][y]=' ';
                x--;
            }
        }
        if(input=='s')
        {
            if(maze[x+1][y]==' ')
            {
                maze[x+1][y]='O';
                maze[x][y]=' ';
                x++;
            }
        }
        if(input=='a')
        {
            if(maze[x][y-1]==' ')
            {
                maze[x][y-1]='O';
                maze[x][y]=' ';
                y--;
            }
        }
        if(input=='d')
        {
            if(maze[x][y+1]==' ')
            {
                maze[x][y+1]='O';
                maze[x][y]=' ';
                y++;
            }
        }
        system("cls");
        showMaze(maze);
    }
        printf("\nyou win!\n");
        return 0;
}
void showMaze(char a[100][100])
{
    int i;
    for(i=0;i<=15;i++)
            puts(a[i]);
}

