#include <stdio.h>
#include <stdlib.h>


int main()
{
    //¥Ú”°√‘π¨
    char maze[100][100]={"######################################",
                       "######################      ###  #####",
                       "O  ################### #### ##        ",
                       "## ###         ####### ####   ########",
                       "## ### ####### ####### ####### #######",
                       "## ### ####### #            ##  ######",
                       "## ### ####### #######################",
                       "##     #######      ##################",
                       "############## ### ###################",
                       "############## ### ###################",
                       "########       #       ####      #####",
                       "####### ############## #### #### #####",
                       "####### ##### ##   ### #### #### #####",
                       "####### ##### #### ### #### #### #####",
                       "####### ##### #### ###      #### #####",
                       "####### ##### #### ############# #####",};
    int i;
    for(i=0;i<=15;i++)
        puts(maze[i]);

    //øÿ÷∆±Ê ∂
    int x=2,y=0;
    while(x!=15||y!=32)
    {
        char input=getch();
        if(input=='w')
        {
            if(maze[x-1][y]==' ')
            {
                maze[x-1][y]='O';
                maze[x][y]=' ';
                x--;
            }
        }
        if(input=='s')
        {
            if(maze[x+1][y]==' ')
            {
                maze[x+1][y]='O';
                maze[x][y]=' ';
                x++;
            }
        }
        if(input=='a')
        {
            if(maze[x][y-1]==' ')
            {
                maze[x][y-1]='O';
                maze[x][y]=' ';
                y--;
            }
        }
        if(input=='d')
        {
            if(maze[x][y+1]==' ')
            {
                maze[x][y+1]='O';
                maze[x][y]=' ';
                y++;
            }
        }
        system("cls");
        for(i=0;i<=16;i++)
            puts(maze[i]);
        }
        printf("you win!\n");
        return 0;
}



